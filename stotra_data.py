def get_all_stotras():
    """Returns list of all stotras with basic info"""
    result = []
    for key, value in STOTRA_DATA.items():
        result.append({
            "key": key,
            "name": value["name"],
            "god": value["god"],
            "symbol": value["symbol"],
            "benefit": value["benefit"],
            "verse_count": len(value["verses"])
        })
    return result


def get_stotra(key):
    """Returns specific stotra by key"""
    return STOTRA_DATA.get(key, None)